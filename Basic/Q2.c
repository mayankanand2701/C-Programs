// Program to input two numbers and perform Arithmetic Operation on it
#include <stdio.h>
int main(void)
{
    int a,b;
    printf("Enter the first number = ");
    scanf("%d",&a);
    printf("Enter the first number = ");
    scanf("%d",&b);
    printf("The Summation of two numbers is = %d \n",(a+b));
    printf("The Subtraction of two numbers is = %d \n",(a-b));
    printf("The Multiplication of two numbers is = %d \n",(a*b));
    printf("The Division of two numbers is = %d \n",(a/b));
}